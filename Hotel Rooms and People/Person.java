public class Person {
    double height;
    int age;
    String name;
    String hairColor;
    boolean Hotel_Full;

    Person(){
        height = 0.0;
        age = 0;
        name = null;
        hairColor = null;
    }
    
    Person(double h, int a, String n, String hC){
        height = h;
        age = a;
        name = n;
        hairColor = hC;
    }
}
